class Personal {
  final String id;
  final String name;

  final String photoUrl;
  final String city;
  final String state;
  final String bio;
  final List<String> specialties;
  final double rating;
  final String whatsapp;
  final double price;

  Personal({
    required this.id,
    required this.name,
    required this.photoUrl,
    required this.city,
    required this.state,
    required this.bio,
    required this.specialties,
    required this.rating,
    required this.whatsapp,
    required this.price,
  });
}
